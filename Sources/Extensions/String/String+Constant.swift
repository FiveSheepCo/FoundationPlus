import Foundation

public extension String {

    /// A static constant representing an empty string.
    static let empty: String = ""
    
    /// A static constant representing a space (" ").
    static let space: String = Character.space.toString
    
    /// A static constant representing a newline ("\n").
    static let newline: String = Character.newline.toString
    
    /// A static constant representing a tab stop ("\t").
    static let tab: String = Character.tab.toString
}
