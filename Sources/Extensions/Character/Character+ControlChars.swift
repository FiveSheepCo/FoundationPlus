import Foundation

public extension Character {
    
    /// A space character.
    /// Represents the most common whitespace character used for separating words in a sentence.
    static let space: Character = " "
    
    /// A newline character.
    /// Used to represent the end of a line of text and the start of a new line.
    static let newline: Character = "\n"
    
    /// A tab character.
    /// Represents a horizontal tabulation used for aligning text in structured formats, often seen in code indentation.
    static let tab: Character = "\t"
}
